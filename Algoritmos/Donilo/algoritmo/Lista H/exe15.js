var elefantes;

function main(){

    elefantes = Number(prompt('Fala quantos elefante meu parcero',))
    var i = 1;
    var palavra = `incomodam, `
    while(i < elefantes){


        span_txt.innerHTML += `1 elefante incomoda muita gente! <br><br>`;


        span_txt.innerHTML += `${i+1} elefantes ${palavra += `incomodam, `} muito mais <br><br>`;
     

        i++;

    }

}